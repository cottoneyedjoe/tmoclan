	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store()
	{
		$save = User::create(Input::all());

		if($save->isSaved())
		{
			return Redirect::route('hello.user')
				->with('flash', 'Your account have been created.');
		}

		return Redirect::route('user.create')
			->withInput()
			->withErrors($save->errors());
	}

